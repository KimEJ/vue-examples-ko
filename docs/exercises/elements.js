const lastLetters = [...new Set(elements.map(el => el.slice(-1)))].sort()

export default {
  question: `Name ${lastLetters.length} chemical elements with different last letters`,
  size: lastLetters.length,

  createChecker() {
    const usedLetters = new Set()
    let index = 0

    return (answer) => {
      const canonical = answer.slice(0, 1).toUpperCase() + answer.slice(1).toLowerCase()

      if (elements.includes(canonical) && !usedLetters.has(canonical.slice(-1))) {
        usedLetters.add(canonical.slice(-1))

        return {
          canonical,
          index: index++
        }
      }

      return false
    }
  }
}
